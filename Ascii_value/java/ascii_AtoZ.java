package Ascii_value.java;
//A to Z = 65 to
// a to z = 97 to
public class ascii_AtoZ
{
    public static void main(String[] args) {
        char ch='a';
       int ascii=ch;
        System.out.println("ascii value of a " +ascii);//97
        // or
        int c1='a';
        System.out.println(c1);//97

        char c='A';
        int Ascii=c;
        System.out.println("Ascii value of A " +Ascii);//65
        //or
        int b='A';
        System.out.println(b);//65

    }
}
